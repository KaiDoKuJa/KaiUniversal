namespace Kai.Universal.Text {

    /// <summary>
    /// WordCase
    /// </summary>
    public enum WordCase {
        /// <summary>
        /// lower camel case
        /// </summary>
        LowerCamel,
        /// <summary>
        /// upper camel case
        /// </summary>
        UpperCamel,
        /// <summary>
        /// lower underscore case
        /// </summary>
        LowerUnderscore,
        /// <summary>
        /// upper underscore case
        /// </summary>
        UpperUnderscore,
        /// <summary>
        /// lower hyphen case
        /// </summary>
        LowerHyphen,
        /// <summary>
        /// upper hyphen case
        /// </summary>
        UpperHyphen,
    }
}